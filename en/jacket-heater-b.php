                     <h5>Main Feature</h5>
                     <p>ULTIMHEAT Flexible jacket heaters are the most effective solution of applying heat to 1000 liters HDPE IBC (Intermediate Bulk Containers). The jacket covers most of the container surface, and heating is provided on the full jacket surface, providing a watt density of ±0.1W/cm², which is 4 to 8 times less than rubber band heaters, and the result is uniform heating without hot spots.</p>

                     <h5>Main Applications</h5>
                     <p>Flexible jacket drum heaters can be used for frost protection, heat up, temperature maintenance, reduce the viscosity or for melting of soaps, greases, varnishes, oils, surfactants, fats (animal & vegetable), foodstuffs and chemicals etc…. They are thermally insulated to improve thermal efficiency.</p>

                     <h5>Technical Features</h5>
                     <p>The heating element of the flexible jacket drum heaters is a silicone insulated heating wire mat, protected by a stitched high strength and water resistant PU/Polyester or Teflon/polyester envelope. 25 mm thick, high temperature resistant foam is put between the heater and the external envelope. This insulation foam has a thermal insulation coefficient (Lambda λ) of 0,039W/mK, and therefore its thermal efficiency is about 3 times higher than a usual 10 mm fiberglass insulated jacket. Quick release buckles allow diameter adjustment, and fast installation and removal</p>

