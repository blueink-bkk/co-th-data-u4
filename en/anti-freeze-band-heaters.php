                     <h1 class="main-title">Anti freeze band heaters</h1>
                     <h3>Main Features</h3>
                     <p>ULTIMHEAT silicone heating elements are made of laminated silicone rubber sheets, vulcanized together through heat and high pressure on both sides of an embedded wire wound heating element. Fiberglass-reinforced silicone rubber gives heater dimensional stability without sacrificing flexibility. Silicone is used because of its high temperature resistance (permanent temperature up to 200°C (390°F), high thermal conductivity (~7 10-4 W/cm.K) and good electrical insulation properties (~12KV/mm)
                     </p>

                     <p>A fiberglass grid, which is visible under the surface, reinforces the silicone rubber sheets. The wire wound heating element web covers the entire surface of the heater. This construction makes silicone rubber heaters an ideal solution to the requirements of many low and medium temperature applications with small sizes or complicated pattern. Their flat shape and small thickness make them highly appreciated for small diameter pipes, without need to modify the existing foam insulation.
                  </p>
                       
                     <h3>Main applications</h3>
                     <p>Silicone flexible band deicing heaters are designed to provide freeze protection to metallic and plastic piping, valves, pumps, water meters and are usually wrapped around the equipments or fixed axially under the pipe insulation foam with cable ties or adhesive tape. We recommend 10 mm thickness insulation foam for pipes up to 24 mm and 20 mm for diameters over this size.
                     </p>
