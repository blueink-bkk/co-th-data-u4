                        -0.1 w/cm² (0.64 w/inch²).  Warning: When used on plastic pipes or foam insulated pipes, do not use higher than 0.1w/cm² (0.64 w/ inch²) watt density to avoid melting plastic pipes or pipe foam insulation.<br>
                        -0.2 w/cm² (1.3 w/inch²).<br>
