                              <td>The oldest technology, dating back to the end of 19th century.
The resistive conductor is coiled around fiberglass or aramid core. (It was asbestos until 50 years ago).
Then the flat heater can be made using 2 technical solutions:<br>
1/- Heating fabric whose warp yarns are made of fiberglass, cotton aramid, polyimide, and weft yarns are made of this coiled heating wires <br>
2/-Stitching the wires on a fabric. This solution allows to use wires with a primary insulation in silicone or FEP. Then the primary insulation can receive a metal braid, for grounding. <strong>This is the single way to produce flexible heaters with a grounding braid,</strong> requested in some industrial applications.</td>
                              <td>The resistive conductor is formed in a flat zig-zag, and assembled in a web. This technology produces heaters <strong> without thickness increase,</strong>similar to the etched models. <strong>The cheapest solution for small quantities, </strong>(Technology patented by Ultimheat)</td>
                              <td>The resistive conductor is coiled around fiberglass or polyimide core. Then a web is made with the coiled wires hand pressed on the un-vulcanized silicone. Then the heating web is vulcanized between 2 sheets of fiberglass reinforced silicone rubber. <br><strong>In their traditional time-consuming assembly process, they are only suited for small quantities.</strong> In the Ultimheat patented technology the fiberglass fabric net and the heating wire are computer designed, and heating wire is automatically embedded in the fiberglass net before vulcanizing. <br><strong>This allows fully automated production and large volume applications.</strong>
</td>
                              <td>The heating element conductive web is made from a thin metal sheet chemically etched with a technology similar to the printed circuits processing. Then this metallic web can be vulcanized between 2 flexible and insulating sheets. Sheets can be made of any type of rubber or elastomers. Because of the larger paths, smaller spacing between elements and better heat transfer, the power density can be up to 2 time bigger than classical wire wound models. <br><strong>They are suited for medium and large quantities.</strong></td>
                              <td>Thick film heaters are constructed on silk screening of conductive and resistive inks. These inks are printed on a flexible substrate. They can be produced using an ink that makes the heater capable of self-regulating its temperature or as a fixed resistance heater.</td>
                              <td>Etched foil Kapton flexible heaters are made with a thin metal foil as the resistance element. The resistance pattern is designed in CAD and transferred to the foil in a process similar to the printed circuits manufacturing. The metal foil is then laminated and bonded to the insulating substrate with adhesive (FEP or Acrylic). The metal foil/substrate is then processed through an acid to produce the etched heating element. The top layer is then added and bonded and laminated with adhesive as done for the first side. Polyimide heater offers dimensional stability and high tensile strength. It is also resistant to most chemicals. (Kapton is a brand name from Dupont for polyimide). </td>
