                  <b>Main uses:</b> Tee equipped with paddle flow switch, for spas and swimming pools applications, mounting on 1” (20 to 21mm ID) soft PVC tubes, adjustable setting.<br>
                  Used on water circulation circuits to detect the passage of water or filter clogging and avoid dry running. Recommended mounting position is on horizontal pipes, but can be mounted in any position allowing an efficient air bleed.<br>
                  <b>Functional principle:</b> Balanced magnetic pallet mounted perpendicular to the flow and activating a reed switch through the wall. The return of the pallet is by made by magnetic action, without spring. No seal or liquid can pass between the piping system and the electrical contact. Suitable for corrosive water pools and spas and salination chlorination and bromination systems. Must not be used for water containing magnetic particles or high viscosity liquids, which block the movement of the pallet.<br>
                  <b>Adjustment:</b> By means of the adjustment screw located under the protective cover. This setting must be carried out only by professional, qualified and trained personnel, as a too low setting can produce an insufficient pull-back force and malfunction. This adjustment is designed for single use and can be sealed.<br>
                  <b>Main housing material:</b> Polypropylene, resistant to ozone and water disinfection products, usable with potable water.<br>
                  <b>Tee material:</b> PVC<br>
                  <b>Paddle:</b> Polypropylene, 15 mm width<br>
                  <b>Paddle shaft:</b> Titanium, providing an outstanding corrosion resistance, and improved mechanical live<br>
                  <b>Electrical rating:</b> Max 1A, Max 70W, Max 250V, resistive load. Use on inductive circuits reduces electrical rating. We recommend to protect the reed switch with contact protection device when used in inductive loads<br>
                  <b>Electric contact type:</b> Normally open, closes by flow rise<br>
                  <b>Liquids compatibility:</b> For use with clean water and liquids without magnetic particles and without chemical incompatibility with polypropylene and titanium<br>
