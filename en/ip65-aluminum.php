              <p><b>Sensing element:</b> Liquid expansion rod. This rod has a non-temperature sensing zone named dead zone which allows thermal insulation crossing. An increased diameter under the thermostat head allows mounting pockets or brackets (See pockets in the accessories section)</p>
             <p><b>Electrical connections:</b> on screw terminal connection block</p>
             <p><b>Earthing:</b> on internal screw terminal</p>
             <p><b>Cable output: </b>M16 cable gland, PA66, for cables up to 10 mm dia.</p>
            <p><b> Mounting:</b> on pockets for liquid immersion or flange for air ducts</p>
