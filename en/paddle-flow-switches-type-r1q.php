                <b>Nominal diameter:</b> Can be used on 32 to 63 mm internal diameter pipes<br>
				The paddle is cleavable and can be cut at various lengths upon pipe diameter. There are cutting lines numbered 1 to 9 every 5mm.<br>
				<b>Recommended mounting position:</b> Vertical, with paddle downside. Other positions are possible with a change in the calibration value related to the paddle weight.<br>
				<b>Water pipe connection:</b> Male thread 1/2”(BSPP) and NBR gasket. Must be used on a BSPP ½” female fitting perpendicular to the main pipe.<br>
				<b>Recommended torque:</b> 7Nm<br>
				<b>Liquids temperature range:</b> 5 to 80°C<br>
				<b>Ambient temperature range:</b> 5 to 50°C<br>
				<b>Ingress protection:</b> IP65<br>
				<b>Electrical connection:</b> 3 x 0.75 mm² cable, PVC insulation, style H05VVF.<br>
				- Pt100 temperature sensor: 3x0.22mm² cable, nickel plated braided FEP insulation
				Flow and temperature connection cables length is the same<br>
				<b>Installation instructions:</b><br>
				- Check carefully the paddle orientation: The arrow on housing must be exactly parallel to the pipe<br>
				- A5 mm minimum gap must be respected between end of the paddle and tube wall opposite to the 3/4” fitting.<br>
				- We recommend the use of nozzles of length less than or equal to 18mm between the gasket seat and the inside of the tube and with an inner diameter greater than or equal to 20mm, to avoid blocking of the pallet<br>
