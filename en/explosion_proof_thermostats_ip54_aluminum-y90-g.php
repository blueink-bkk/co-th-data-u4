               <h5 class="text-center">General rules for installation:</h5>
               <p> This thermostat uses a flameproof micro-switch type Ex II2G (Ex dIIC T6 Gb). It is installed in a waterproof aluminum enclosure with an ingress protection class IP54, grey epoxy painted RAL 7032, fitted with a built in electrical connection cable. It is not allowed to make electrical connections inside this aluminum enclosure. Electrical connection at the end of this cable must be made inside an increased safety "e" junction box complying with the standard EN60079-7, or outside hazardous area. Aluminum enclosure earthing is mandatory.<br>
              <strong>Two versions:</strong> with internal adjustment requiring opening the cover, or with a window and internal adjustment access without removing the cover.<br>
              <strong>Housing:</strong> aluminum with wall mounting plate in stainless steel.<br>
              <strong>Dimensions:</strong> 104 x 102 x 86 mm without window , 104 x 102 x 91.3mm with window<br>
              <strong>Bulb and capillary:</strong> Copper for ranges up to 50 ° C. Stainless steel for ranges above. Capillary length 1500mm or 3000mm. Minimum bending radius of 5 mm.<br>
              <strong>Temperature sensing element:</strong> liquid expansion, based on hydrocarbon oils. Side outlet capillary, protected by flexible metal stainless steel sheath.<br>
              <strong>Electrical connection:</strong> by H05VVF style cable, 3x 0.5 mm², length 3 meters, protected by corrugated flexible stainless steel sheath. Other lengths on request : 0.3m, 1m, 2m, 10m<br>
              <strong>Setting:</strong> by knob graduated in °C. Version with ° F printing on request. The adjustment shaft is in a separate area of the electrical contact area, so the adjustment is possible when power is on.<br>
              Mounting: by wall plate, 4 holes dia.5.5mm, 94mm x 92mm axis distance. by wall plate, 4 holes dia.5.5mm, 94mm x 92mm axis distance.<br>
              <strong>Contacts:</strong> SPDT (snap action contact)<br>
              <strong>Electrical rating:</strong> Suitable for remote control of relay coils or PLCs circuits.</p>
