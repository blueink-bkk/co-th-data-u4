               <h5 class="text-center">Main features</h5>
               <p>This electronic temperature controller with the simplest and the most instinctive setting by end user was designed for easy incorporation inside cabinets with DIN rail mounting.<br>
                 It can be used by untrained operators. <br>
                 It provides simple On Off action temperature control.<br>
                 End user has access to set point and differential setting only.<br>
                  It is possible to set °C or °F display, heating or cooling relay output, decimal display, sensor type and temperature range by internal Dip switches (Without access by end user).<br>
                Adjustment of maximum temperature can be set without need to open the control.</p>
                <p><b>Dimensions:</b> 86.9 x 58 x 52.5 mm </p>
                <p><b>Display:</b> 3+1 digit LED. The fourth digit is used to display °C or °F, upon setting made.</p>
               <p> <b>Set point setting:</b> In normal use, the display shows measured temperature. Push “+” or “-” keys will display the set point value, and at that time it can be adjusted with “+” and “-” keys. No action during 5 seconds will register the new set point value and bring back display to measured value.</p>
              <p><b>Temperature differential setting:</b> In normal use, the display shows measured temperature. Push “D” key will display the differential value, at that time it can be adjusted with “+” and “-” keys. Push “D” again or no action during 5 seconds will register the new differential value and bring back display to measured value.</p>
              <p><b>Action:</b> On-OFF</p>
              <p><b>Temperature sensor:</b> Pt100 (2 or 3 wires) or NTC 10Kohms @25°C, B= 3380 (2 wires) Temperature sensor can be selected by a dip switch on circuit (No access to final user)</p>
              <p><b>Accuracy:</b> +/-1% of scale</p>
                <p><b>Temperature adjustment ranges:</b>
                - 30+120°C (-20+250°F), with 1° display<br>
                - 30,0 to +40,0°C (-20,0 + 99,9°F), with 1/10° display<br>
                - 30+400°C (-20+750°F), with 1° display<br>
                Temperature range and decimal digit can be selected by a dip switch on circuit (No access to final user)</p>
                  <p><b> Power supply:</b> 90 to 240V, 50Hz or 60Hz</p>
   <p><b>Relay output:</b> SPNO, 16A250V res., 100.000 cycles. Output Led displays relay position.</p>
   <p><b>Relay action:</b> Heating or cooling, open or close on temperature rise output relay action can be selected by a dip switch on circuit (No access to final user)</p>
        <p><b>°C or °F display:</b> can be selected by a dip switch on circuit (No access to final user)</p>
           <p><b>Maximum possible set point adjustment by user:</b> Push “D” button more than 10 seconds, display shows the maximum temperature that can be set by user. Then it is possible to adjust this value with “+” and “-“. push again on “D” or do nothing during 5 seconds will register the maximum possible setting value and control will comes back to measured valued</p>
<p><b>Ambiant</b>-20+60°C, 10-90% RH</p>
<p><b>Power:</b> <4W</p>
<p><b>Fail safe safety:</b><br>
- If no power supply, relay output contact will open<br>
- If Pt100 sensor or NTC is broken or not connected properly, relay output contact will open and display will show “EEE”<br>
- If measured temperature is higher than allowed by the set range, display will show “HHH”<br>
- If measured temperature is lower than -30,0°C or -20,0°F, display will show “LLL”</p>
<p><b>Electrical connections:</b><br>
- Power input: Neutral, phase, ground, with 2.5 mm² terminals<br>
- Power output: Neutral, phase, ground, with 2.5 mm² terminals for direct connection to the load.<br>
- Temperature sensor: three 2.5 mm² screw terminal<br>
One removable jumper provides a potential free relay output for applications needing a separate circuit for relay, external timer or other.</p>
    <p><b>Internal parameters setting:</b> Process is given on request to approved distributors. This allows to store only one product and set parameters upon end user request.</p>
<p><b>Standards:</b> Comply with EMC (CE), ROHS and Reach</p>
