               <h5 class="text-center">Main features</h5>
                <b>Main applications:</b> Product developed for electrical or gas instantaneous water heaters. 3/4 “ BSPP male water inlet and outlet. Mandatory vertical mounting, with water inlet from the bottom. Built-in over-pressure valve<br>
