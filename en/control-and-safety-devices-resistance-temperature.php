                        <p>They are divided in 2 major technologies:<br>
                          <b>1. Wire coil or deposited film of pure metal</b><br>
                          The element's resistance increases with temperature. They have a very good repeatability and excellent accuracy over a wide temperature range. Temperature range : -260 oC to 850 oC . Platinum RTD's produces a more linear response than thermocouples or thermistors. Therefore, Electronic temperature controls using these RTD are simpler and cheaper than controls using thermocouples. They do not need special connection wires like thermocouples.<br>

                          They are standardized by IEC 751. The most common models are Platinum 100 ohms and Platinum 1000 ohms. They are worldwide used and interchangeable. For standard accuracy measurement, they are connected with 2 wires. 3 or 4 wires connection can be requested for high accuracy temperatures measurement to compensate wire resistance.<br>

                          Their use in flexible heating elements can be risky, as most of them are not flexible, and have much larger dimensions than thermocouples ( 2 x 5 x 1 mm is usually the smallest possible size)<br>

                          <b>2. NTC: they use resistivity change against temperature of mixed metal oxides</b><br>
                          The element's resistance decreases with temperature. They have a poor repeatability and medium accuracy limited to a short temperature range. They are not linear. Interchangeability is limited, even using products coming from the same supplier.<br>

                          Their main advantage is their very small size, very low cost. They are commonly used in appliances with low cost electronic controls. They just need 2 standard wires for connection When using the smallest size they can be built in heaters without increasing its thickness and without changes in its flexibility.</p>
