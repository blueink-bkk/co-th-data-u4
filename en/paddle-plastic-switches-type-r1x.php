                  <b>Main uses:</b> General application in flow detection. Recommended mounting position is on horizontal pipes, but can be mounted in any position. For water flow detection on dia. 15 to 100 mm water pipes<br>
                  Functional principle:<br>
                  Balanced magnetic paddle mounted perpendicular to the flow and activating a reed switch through the wall. The return of the paddle is by made by magnetic action, without spring. No seal or liquid can pass between the piping system and the electrical contact. Suitable for corrosive water pools and spas and salination chlorination and bromination systems. Must not to be used for water containing magnetic particles or high viscosity liquids, which block the movement of the paddle.<br>
                  <b>Adjustment:</b><br>
                  - By cleaving the paddle<br>
                  - Fine adjustment by screw driver on internal dial (on models with connection box only)<br>
                  <b>Paddle shaft:</b> Titanium, providing an outstanding corrosion resistance, and improved mechanical live. Suitable for corrosive water pools and spas and salination chlorination and bromination systems<br>
                  <b>Main housing material:</b> PPO, fiber glass reinforced for improved pressure resistance, usable with potable water.<br>
                  <b>Paddle:</b> PPO, 12 mm width, can be cleaved into 4 sections numbered 1 to 4 for pipe diameter adjustment<br>
                  <b>Pipe mounting:</b> Fiber glass reinforced union nut,¾” BSPP, mounting on ¾” BSPP male fitting with gasket. Recommended torque: 7±1 Nm<br>
                  <b>Gasket:</b> NBR<br>
                  <b>Electrical rating:</b> Max 1A, Max 70W, Max 250V, resistive load. Use on inductive circuits reduces electrical rating. We recommend to protect the reed switch with contact protection device when used in inductive loads<br>
                  <b>Electric contact type:</b> Normally open, closes by flow rise<br>
                  <b>Liquids compatibility:</b> For use with clean water and liquids without magnetic particles and without chemical incompatibility with PPO and titanium<br>
                  <b>Nominal pressure at 20°C:</b> 1MPa (PN10)<br>
                  <b>Liquids temperature range:</b> 5 to 100°C<br>
                  <b>Ambient temperature range:</b> 5 to 80°C<br>
                  <b>Ingress protection:</b> IP65<br>
